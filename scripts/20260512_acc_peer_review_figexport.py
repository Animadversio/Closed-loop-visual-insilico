def load_neural_session(session_id: str) -> tuple[pd.DataFrame, np.ndarray, list[int]]:
    """Load accentuation session: returns (df_allstim_w_resp, respmat, unit_list)."""
    data_path = join(EPHYS_DIR, "vvs-accentuate_controlsessions_5monkeys_250504-250512.h5")
    data = load_from_hdf5(data_path)

    respmat    = data[session_id]["repavg"]["response_peak"]          # (n_stims, n_units)
    stimnames  = data[session_id]["repavg"]["stimulus_name"]
    stimnames_str = [s.decode("utf-8") if isinstance(s, bytes) else s for s in stimnames]

    print(f"Loaded {session_id}: {respmat.shape[0]} stims, {respmat.shape[1]} units")

    df = parse_accentuated_filenames_to_df(stimnames_str, include_parse_fail=True)

    # Attach neural responses for all driven units
    unit_list = sorted([int(u) for u in df.unit_id.dropna().unique()])
    for unit in unit_list:
        df[f"neural_resp_unit_{unit}"] = respmat[:, unit]

    return df, respmat, unit_list


def load_model_predictions(encoding_session_id: str) -> tuple[pd.DataFrame, pd.DataFrame]:
    """Load model predicted responses for NSD and accentuated stimuli."""
    posthoc_dir = join(ENCODING_DIR, encoding_session_id, "posthoc_model_predict")

    df_encoding = pd.read_pickle(
        join(posthoc_dir, f"encoding_stim_info_w_pred_resp_{encoding_session_id}.pkl"))
    df_accentuated = pd.read_pickle(
        join(posthoc_dir, f"accentuated_stim_info_w_pred_resp_{encoding_session_id}.pkl"))
    df_accentuated["basename"] = df_accentuated["filepath"].apply(os.path.basename)

    return df_encoding, df_accentuated


def build_nsd_df(df_allstim: pd.DataFrame, df_encoding: pd.DataFrame) -> pd.DataFrame:
    """Merge NSD natural images with model predictions."""
    df_nat = df_allstim.query("model_name.isna()").copy()
    df_nat = df_nat.drop(columns=["model_name", "unit_id", "img_id", "level", "score"],
                         errors="ignore")

    # Fix is_test column if stored as mixed types
    df_enc = df_encoding.copy()
    df_enc["is_test"] = df_enc["is_test"].map(
        {1: True, 0: False, "1": True, "0": False, "True": True, "False": False,
         True: True, False: False}).astype(bool)

    df_merged = pd.merge(df_nat, df_enc,
                         how="inner",
                         left_on="filepath",
                         right_on="stimulus_name")
    print(f"NSD merged: {len(df_merged)} rows")
    return df_merged


def build_accentuated_df(df_allstim: pd.DataFrame,
                         df_acc_pred: pd.DataFrame) -> pd.DataFrame:
    """Merge accentuated stimuli model predictions with neural responses."""
    df_merged = df_acc_pred.merge(
        df_allstim,
        left_on=["basename", "img_id", "unit_id", "model_name"],
        right_on=["filepath", "img_id", "unit_id", "model_name"],
        how="inner",
        suffixes=("", "_dfallstim"),
    )
    # Sanity check: score and level must agree between tables
    for col in ["score", "level"]:
        col2 = f"{col}_dfallstim"
        if col2 in df_merged.columns:
            assert np.allclose(
                df_merged[col].astype(float),
                df_merged[col2].astype(float),
                atol=1e-5, equal_nan=True,
            ), f"Column '{col}' mismatch between tables"
    print(f"Accentuated merged: {len(df_merged)} rows, units: {sorted(df_merged.unit_id.unique())}")
    return df_merged



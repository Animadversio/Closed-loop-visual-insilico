# Main
# ---------------------------------------------------------------------------
def main():
    # Load data
    df_allstim, respmat, unit_list = load_neural_session(ACC_SESSION_ID)
    df_encoding, df_acc_pred       = load_model_predictions(ENCODING_SESSION_ID)

    df_nsd_merged = build_nsd_df(df_allstim, df_encoding)
    df_acc_merged = build_accentuated_df(df_allstim, df_acc_pred)

    for unit_id in UNIT_IDS:
        print(f"\n--- Unit {unit_id} ---")

        # Fig 1: NSD validation
        fig = fig_nsd_stacked(df_nsd_merged, unit_id, REVIEWER_LIST)
        saveallforms(FIGDIR, f"acc_peer_review_{ACC_SESSION_ID}_U{unit_id}_NSD_stacked", figh=fig)
        plt.close(fig)

        # Fig 2: Accentuated 2×2
        fig = fig_acc_2x2(df_acc_merged, unit_id, ACCENTUATOR_LIST, REVIEWER_LIST)
        saveallforms(FIGDIR, f"acc_peer_review_{ACC_SESSION_ID}_U{unit_id}_acc_2x2", figh=fig)
        plt.close(fig)

        print(f"  Saved figures for unit {unit_id} → {FIGDIR}")

    print("\nDone.")


if __name__ == "__main__":
    main()

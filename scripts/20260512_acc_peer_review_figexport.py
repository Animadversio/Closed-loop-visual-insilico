# Plot helpers
# ---------------------------------------------------------------------------
def _scatter_with_stats(ax, df, xvar, yvar, hue=None, palette="tab10"):
    """Scatter plot with y=x line and r/R²/N annotation."""
    scatter_kwargs = dict(data=df, x=xvar, y=yvar, alpha=0.6,
                          legend=(hue is not None), ax=ax)
    if hue is not None:
        scatter_kwargs.update(hue=hue, palette=palette)
    sns.scatterplot(**scatter_kwargs)

    x, y = df[xvar], df[yvar]
    mask = x.notna() & y.notna()
    if mask.sum() > 1:
        corr, _ = pearsonr(x[mask], y[mask])
        r2      = r2_score(y[mask], x[mask])
    else:
        corr = r2 = float("nan")

    # Equal axes + y=x line
    lims = [min(ax.get_xlim()[0], ax.get_ylim()[0]),
            max(ax.get_xlim()[1], ax.get_ylim()[1])]
    ax.set_xlim(lims); ax.set_ylim(lims)
    ax.axline((0, 0), slope=1, linestyle="--", color="gray", lw=1)

    ax.text(0.97, 0.05,
            f"r = {corr:.2f}\nR² = {r2:.2f}\nN = {mask.sum()}",
            transform=ax.transAxes, ha="right", va="bottom",
            fontsize=10, bbox=dict(boxstyle="round", fc="white", alpha=0.7))

    ax.set_xlabel("Predicted response")
    ax.set_ylabel("Neural response")


# ---------------------------------------------------------------------------
# Figure 1: NSD validation — stacked reviewer panels
# ---------------------------------------------------------------------------
def fig_nsd_stacked(df_merged: pd.DataFrame, unit_id: int,
                    reviewer_list: list[str]) -> plt.Figure:
    """Neural vs predicted on NSD images, one panel per reviewer model."""
    yvar = f"neural_resp_unit_{unit_id}"
    fig, axes = plt.subplots(len(reviewer_list), 1,
                             figsize=(4, 4 * len(reviewer_list)),
                             sharex=True, sharey=True)
    if len(reviewer_list) == 1:
        axes = [axes]

    for ax, reviewer in zip(axes, reviewer_list):
        xvar = f"pred_resp_{reviewer}_unit_{unit_id}"
        _scatter_with_stats(ax, df_merged, xvar, yvar, hue=None)
        ax.set_title(f"Reviewer: {reviewer}", fontsize=10)

    fig.suptitle(f"NSD images — {ACC_SESSION_ID}\nUnit {unit_id}", fontsize=12)
    fig.tight_layout()
    return fig


# ---------------------------------------------------------------------------
# Figure 2: Accentuated 2×2 (accentuator × reviewer)
# ---------------------------------------------------------------------------
def _get_palette(n_colors: int, palette: str = "tab10") -> list:
    return sns.color_palette(palette, n_colors)


def fig_acc_2x2(accentuated_merged_df: pd.DataFrame, unit_id: int,
                accentuator_list: list[str],
                reviewer_list: list[str]) -> plt.Figure:
    """
    2x2 grid: rows = accentuator model, cols = reviewer model.
    y = neural response, x = reviewer predicted response.
    Points colored by img_id. Dots connected by thin colored lines per img_id.
    Stats annotation placed outside each panel on the right.
    """
    yvar   = f"neural_resp_unit_{unit_id}"
    n_rows = len(accentuator_list)
    n_cols = len(reviewer_list)

    fig, axes = plt.subplots(n_rows, n_cols,
                             figsize=(5 * n_cols, 4.5 * n_rows),
                             constrained_layout=True,
                             sharex=True, sharey=True)
    if n_rows == 1 and n_cols == 1:
        axes = np.array([[axes]])
    elif n_rows == 1:
        axes = axes[np.newaxis, :]
    elif n_cols == 1:
        axes = axes[:, np.newaxis]

    for i, accentuator in enumerate(accentuator_list):
        df_sub  = accentuated_merged_df.query(
            "model_name == @accentuator and unit_id == @unit_id"
        ).copy()
        img_ids = sorted(df_sub["img_id"].unique())
        palette = _get_palette(len(img_ids))
        color_map = {iid: palette[k] for k, iid in enumerate(img_ids)}

        for j, reviewer in enumerate(reviewer_list):
            ax   = axes[i, j]
            xvar = f"pred_resp_{reviewer}_unit_{unit_id}"

            # --- compute limits from data BEFORE plotting so nothing is clipped ---
            x_vals = df_sub[xvar].dropna()
            y_vals = df_sub[yvar].dropna()
            pad  = 0.05 * (max(x_vals.max(), y_vals.max()) -
                           min(x_vals.min(), y_vals.min()))
            vmin = min(x_vals.min(), y_vals.min()) - pad
            vmax = max(x_vals.max(), y_vals.max()) + pad
            ax.set_xlim(vmin, vmax)
            ax.set_ylim(vmin, vmax)

            # --- connected lines per img_id (behind dots) ---
            for iid in img_ids:
                grp = df_sub[df_sub["img_id"] == iid].sort_values("level")
                ax.plot(grp[xvar], grp[yvar],
                        color=color_map[iid], alpha=0.5, lw=0.8, zorder=1)

            # --- scatter (larger markers) ---
            for iid in img_ids:
                grp = df_sub[df_sub["img_id"] == iid]
                ax.scatter(grp[xvar], grp[yvar],
                           color=color_map[iid], s=50, alpha=0.7,
                           zorder=2, label=str(iid))

            # --- y=x reference line ---
            ax.axline((0, 0), slope=1, linestyle="--", color="gray", lw=1)

            # --- stats inside panel, top left ---
            mask = df_sub[xvar].notna() & df_sub[yvar].notna()
            if mask.sum() > 1:
                corr, _ = pearsonr(df_sub.loc[mask, xvar], df_sub.loc[mask, yvar])
                r2      = r2_score(df_sub.loc[mask, yvar], df_sub.loc[mask, xvar])
            else:
                corr = r2 = float("nan")
            ax.text(0.05, 0.95,
                    f"r = {corr:.2f}\nR² = {r2:.2f}\nN = {mask.sum()}",
                    transform=ax.transAxes, ha="left", va="top",
                    fontsize=9, bbox=dict(boxstyle="round", fc="white",
                                         ec="lightgray", alpha=0.85))

            # --- legend only on the final panel (bottom-right) ---
            is_last = (i == len(accentuator_list) - 1) and (j == len(reviewer_list) - 1)
            if is_last:
                ax.legend(title="img_id", bbox_to_anchor=(1.01, 1),
                          loc="upper left", fontsize=7, title_fontsize=8,
                          framealpha=0.8)
            else:
                leg = ax.get_legend()
                if leg: leg.remove()

            ax.set_xlabel("Predicted response")
            ax.set_ylabel("Neural response")
            ax.set_title(f"Accentuator: {accentuator}\nReviewer: {reviewer}", fontsize=9)
            ax.tick_params(left=True, bottom=True, labelleft=True, labelbottom=True)

    fig.suptitle(
        f"Accentuated images — {ACC_SESSION_ID}\nUnit {unit_id}",
        fontsize=13)
    return fig


